public class Ej1 {
    public static void main(String[] args) {
        int x = 144;
        int y = 999;
        System.out.println("La variable x contiene el valor: "+x);
        System.out.println("La variable y contiene el valor: "+y);
        System.out.println("La suma de x e y es: "+(x+y));
        System.out.println("La resta de x e y es: "+(x-y));
        System.out.println("La multiplicación de x e y es: "+(x*y));
        System.out.println("La división de x e y es: "+(x/y));
    }
}
