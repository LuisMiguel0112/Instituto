
/*
*   Mostrar los números del 320 al 160, contando de 20 en 20 hacia atrás (for).
*
*   @author Luis Miguel García Sevilla - 1º DAM.
*/

package Tema5.C5_2;

public class Ej4 {
  public static void main(String[] args) {
    for (int i = 320; i>=160; i = i-20){
      System.out.println(i);
    }
  }
}
